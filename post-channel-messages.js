const client = new Client({ intents: [GatewayIntentBits.Guilds] });

client.once('ready', async () => {
  console.log(`\n✅ ログイン: ${client.user.tag}`);
  const guild = client.guilds.cache.first();
  await guild.channels.fetch();

  const step = async (label, fn) => {
    try { await fn(); console.log(`  ✅ ${label}`); }
    catch (e) { console.log(`  ⚠️  ${label} — ${e.message}`); }
    await sleep(800);
  };

  for (const [chName, text] of Object.entries(MESSAGES)) {
    const ch = guild.channels.cache.find(c => c.name === chName);
    if (!ch) { console.log(`  ⚠️  #${chName} が見つかりません`); continue; }

    await step(`#${chName} に投稿`, async () => {
      const msg = await ch.send(text);
      await sleep(400);
      await msg.pin();
    });
  }

  console.log('\n══════════════════════════════════════');
  console.log('  ✅ 全チャンネルへの投稿・ピン留め完了');
  console.log('══════════════════════════════════════\n');
  process.exit(0);
});

client.on('error', e => { console.error(`❌ ${e.message}`); process.exit(1); });
client.login(process.env.DISCORD_TOKEN).catch(e => { console.error(`❌ ${e.message}`); process.exit(1); });
